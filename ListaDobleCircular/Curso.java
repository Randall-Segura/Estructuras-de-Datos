public class Curso {
    
    //atributos
    
    private int id;
    private String nombreCurso;
    
    //constructor vacio

    public Curso() {
        this.id = 0;
        this.nombreCurso = "";
    }
    
    
    //getters and setters

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombreCurso() {
        return nombreCurso;
    }

    public void setNombreCurso(String nombreCurso) {
        this.nombreCurso = nombreCurso;
    }
    
    
    
    
    
    
    
    
}
