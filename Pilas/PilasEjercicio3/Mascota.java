
package sem4pilas3;


public class Mascota {
    
    
    
    //atributos
    
    private String nombre;
    private String tipo;
    
    //constructor vacio
    
    public Mascota(){
        this.nombre="";
        this.tipo="";
    }
    
    
    //geters and setters

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
}
